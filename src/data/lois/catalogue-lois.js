export function getLoisParBloc(bloc) {
  return CATALOGUE_LOIS[`BLOC_${bloc.toUpperCase()}`] ?? []
}

export function getLoisDisponibles(etatJeu) {
  const toutes = Object.values(CATALOGUE_LOIS).flat()
  return toutes.filter(loi => {
    const c = loi.conditions
    if (c.popularite_min && etatJeu.popularite_joueur < c.popularite_min) return false
    if (c.reserve_min && etatJeu.reserve_budgetaire_milliards < c.reserve_min) return false
    if (c.stabilite_min && etatJeu.stabilite < c.stabilite_min) return false
    if (c.stabilite_max && etatJeu.stabilite > c.stabilite_max) return false
    if (c.prix_baril_min && etatJeu.prix_baril < c.prix_baril_min) return false
    if (c.scandale_actif && !etatJeu.scandale_actif) return false
    return true
  })
}

export function getToutesLois() {
  return Object.values(CATALOGUE_LOIS).flat()
}
